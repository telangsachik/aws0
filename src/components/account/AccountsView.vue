<template>
  <div>
    <p class='text-center text-bold text-grey-9 selector-title'>
      Select an account
    </p>
    <AccountsInnerView :persistent='persistent' :searchable='true' @selected='onOwnerSelected' />
  </div>
</template>

<script setup lang='ts'>
import { toRef } from 'vue'
import { db } from 'src/model'

import AccountsInnerView from './AccountsInnerView.vue'

interface Props {
  persistent?: boolean
}
const props = withDefaults(defineProps<Props>(), {
  persistent: true
})
const persistent = toRef(props, 'persistent')

const owner = defineModel<db.Owner>()
const emit = defineEmits<{(ev: 'selected', value: db.Owner): void}>()

const onOwnerSelected = (_owner: db.Owner) => {
  owner.value = _owner
  emit('selected', _owner)
}

</script>
